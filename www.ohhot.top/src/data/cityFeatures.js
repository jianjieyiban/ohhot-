// 获取城市特色信息
export const getCityFeatures = (cityName) => {
  return cityFeatures[cityName] || {
    industries: [],
    avgSalary: '未知',
    talentDemand: '未知',
    keyCompanies: [],
    advantages: [],
    challenges: []
  };
};

// 根据行业筛选城市
export const getCitiesByIndustry = (industry) => {
  return Object.keys(cityFeatures).filter(city => 
    cityFeatures[city].industries.includes(industry)
  );
};

// 根据薪资范围筛选城市
export const getCitiesBySalaryRange = (minSalary, maxSalary) => {
  return Object.keys(cityFeatures).filter(city => {
    const salaryRange = cityFeatures[city].avgSalary;
    const [min, max] = salaryRange.split('-').map(s => parseInt(s.replace('k', '000')));
    return min >= minSalary && max <= maxSalary;
  });
};